    MSP Enterprise Co. 創立於1987年，初期致力於機車排氣管製造，其中以「速克達」機車賽事用之排氣管為主力產品，並擁有許多優秀成績﹔後期由於國內市場需求改變與大量國外資訊導入，有鑒於高性能避震器在全球市場更具發展潛力與空間，於是著手研發製造高性能避震器，並導入多項測試儀器以利協助開發，多年來本公司所研發生產之MD-R系列避震統，已獲得國內外廣大消費群的支持與信賴。</br></br>
    此外，MSP在日本車隊長期配合協助研發調校與使用之下，多次參加日本Motor Champ杯全國大賽，皆獲得極高的評價與優秀成績。</br></br>
    在2007年贏得全日本速克達FP&SP4 雙料冠軍，並在2008年獲得年度積分總冠軍。於是在日本速克達和12吋輕型檔車比賽之中，MSP已是各知名車隊指名選用避震系統之一。</br></br>
    2009年，MSP推出DDS與系列產品，DDS系列已在國內外各比賽中，獲得許多優秀成績。</br>
